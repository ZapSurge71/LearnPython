# TIME
import time
# Here, we are "including" a module, code written by others to serve a purpose,
# called "time".

# Using time to delay code execution
print("We use time.sleep(~~~) to...")
time.sleep(1.5)
print("...delay lines of code from executing")

# We can use time.delay() for dialogue
print("Wanna see me run to that mountain and back?")
time.sleep(2)
print("Ya wanna see me do it again?")
