# INTEGERS (int type)
# Whole numbers.
a= 3
b= -4

# FLOATS (float type)
# Decimal numbers
x= 0.2
y= 3.0

# BOOLEANS (bool type)
# True or False
summer= True
cold= False

# STRINGS (str type)
# Series of characters (letters, numbers, punctuation, etc.)
greeting= "Hey Bob! How's it going?"
car= "2017 Porsche 911 Carrera"

# NO VALUE (NoneType type)
# "None" is the "null" in Python
nothing= None
tokens= None

